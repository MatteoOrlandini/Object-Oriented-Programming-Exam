package exam;

public class StringStats {
	String field;
	double count;
	
	public StringStats(String field, double count) {
		super();
		this.field = field;
		this.count = count;
	}

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

	public double getCount() {
		return count;
	}

	public void setCount(double count) {
		this.count = count;
	}

	
}
